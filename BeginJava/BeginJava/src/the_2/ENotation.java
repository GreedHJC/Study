package the_2;

public class ENotation {
	public static void main(String[] arg) {
		double e1 = 1.2e-3;
		double e2 = 1.2e+3;
		
		int num1 = 0xA0E;				//0x 16진수
		int num2 = 0752;				//0  8 진수
		
		System.out.println(e1);
		System.out.println(e2);
		System.out.println(num1);
		System.out.println(num2);
		
	}
}
